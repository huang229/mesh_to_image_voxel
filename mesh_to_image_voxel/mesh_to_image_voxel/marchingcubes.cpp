struct XYZ
{
	float x{ 0.f }, y{ 0.f }, z{ 0.f };
	static float epsilon;

	static inline char comparefloat(const float& x, const float& y)
	{
		const auto sub = x - y;
		if (sub < -epsilon)
		{
			return -1;
		}
		if (sub > epsilon)
		{
			return 1;
		}
		return 0;
	}

	bool operator<(const XYZ& other) const
	{
		// return (this->x < other.x)
		// || ((this->x == other.x) && (this->y < other.y))
		// || ((this->x == other.x) && (this->y == other.y) && (this->z < other.z));

		return (comparefloat(this->x, other.x) < 0)
			|| ((comparefloat(this->x, other.x) == 0) && (comparefloat(this->y, other.y) < 0))
			|| ((comparefloat(this->x, other.x) == 0) && (comparefloat(this->y, other.y) == 0) && (comparefloat(this->z, other.z) < 0));
	}
};


